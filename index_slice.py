# Indexing & Slicing of Strings

#Indexing 
word = "Welcome to Python"
print("Original Word: ",word)
print(f"Index of W : {word.index('W')}")

#Slicing 
sample_string = "Hey there!, I'm Aditya"
sub_string1 = sample_string[0:3]
sub_string2 = sample_string[12:]
print("Original string: ",sample_string)
print("Substring 1 : ",sub_string1)
print("Substring 2 : ",sub_string2)

